    private void getEmprestado() {
        EmprestimoDAO dao = new EmprestimoDAO();
        List<Emprestimo> lstEmprestimo = dao.getEmprestado();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Titulo");
        model.addColumn("Pessoa");
        model.addColumn("Data Emprestimo");
        model.addColumn("Data Devolucao");
        model.addColumn("Data Devolvido");
        tableDevolucao.setModel(model);
        for (Emprestimo emprestimo : lstEmprestimo) {
            Object row[] = {
                emprestimo.getLivro().getTitulo(),
                emprestimo.getPessoa().getNome(),
                emprestimo.getDataEmprestimo(),
                emprestimo.getDataDevolucao(),
                emprestimo.getDataDevolvido()
            };
            model.addRow(row);
        }
    }

    private boolean validarDevolucao() {
        return tableDevolucao.getSelectedRowCount() != 0;
    }

