    private void btnDevolver1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolver1ActionPerformed
        if (validarDevolucao()) {
            if (tableDevolucao.getRowCount() > 0) {
                EmprestimoDAO dao = new EmprestimoDAO();
                dao.devolver(tableDevolucao.getSelectedRow(), LocalDate.now());
                getEmprestado();
                JOptionPane.showMessageDialog(this, "Devolução realizada com sucesso!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Necessario selecionar uma livro emprestado para realizar a devoluçao");
        }
    }//GEN-LAST:event_btnDevolver1ActionPerformed

