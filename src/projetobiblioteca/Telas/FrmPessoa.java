    private void getPessoa() {
        PessoaDAO dao = new PessoaDAO();
        List<Pessoa> lstPessoa = dao.listar();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nome");
        model.addColumn("CPF");
        model.addColumn("E-mail");
        model.addColumn("Telefone");
        tablePessoa.setModel(model);
        for (Pessoa pessoas : lstPessoa) {
            Object row[] = {
                pessoas.getNome(),
                pessoas.getCpf(),
                pessoas.getEmail(),
                pessoas.getTelefone()
            };
            model.addRow(row);
        }
    }

    private void limparCampos() {
        i = -1;
        txtNome.setText("");
        txtCpf.setText("");
        txtEmail.setText("");
        txtTelefone.setText("");
        txtNome.requestFocus();
    }

    private boolean validarCampos() {
        boolean ret = true;
        if (txtNome.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "NOME DA PESSOA";
            txtNome.requestFocus();
            return ret;
        }
        if (txtCpf.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "CPF DA PESSOA";
            txtCpf.requestFocus();
            return ret;
        }
        if (txtEmail.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "EMAIL DA PESSOA";
            txtEmail.requestFocus();
            return ret;
        }
        if (txtTelefone.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "TELEFONE DA PESSOA";
            txtTelefone.requestFocus();
            return ret;
        }
        return ret;
    }

