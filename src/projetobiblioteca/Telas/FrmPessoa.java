    private void tablePessoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePessoaMouseClicked
        if (evt.getClickCount() == 1) {
            txtNome.setText(tablePessoa.getValueAt(tablePessoa.getSelectedRow(), 0).toString());
            txtCpf.setText(tablePessoa.getValueAt(tablePessoa.getSelectedRow(), 1).toString());
            txtEmail.setText(tablePessoa.getValueAt(tablePessoa.getSelectedRow(), 2).toString());
            txtTelefone.setText(tablePessoa.getValueAt(tablePessoa.getSelectedRow(), 3).toString());
            i = tablePessoa.getSelectedRow();
            pessoaAlterada = new Pessoa(txtNome.getText(), txtCpf.getText(), txtEmail.getText(), txtTelefone.getText());
        }
    }//GEN-LAST:event_tablePessoaMouseClicked

