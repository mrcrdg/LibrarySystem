    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tablePessoa.getRowCount() > 0) {
            PessoaDAO dao = new PessoaDAO();
            dao.Excluir(tablePessoa.getSelectedRow());
            limparCampos();
            getPessoa();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvar1ActionPerformed
        if (validarCampos()) {
            pessoa = new Pessoa(txtNome.getText(), txtCpf.getText(), txtEmail.getText(), txtTelefone.getText());
            if (i == -1) {
                PessoaDAO dao = new PessoaDAO();
                dao.Salvar(pessoa);
            } else if (i > -1) {
                PessoaDAO dao = new PessoaDAO();
                dao.alterar(pessoaAlterada, pessoa);
            }
            getPessoa();
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "O Campo " + campoObrigatorio + " Ã© de preenchimento obrigatorio!");
        }
    }//GEN-LAST:event_btnSalvar1ActionPerformed

