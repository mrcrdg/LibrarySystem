    private void getLivro() {
        LivroDAO dao = new LivroDAO();
        lstLivro = dao.listar(true);
        cbLivro.removeAllItems();
        for (int i = 0; i < lstLivro.size(); i++) {
            cbLivro.addItem(lstLivro.get(i).getTitulo());
        }
    }

    private void getPessoa() {
        PessoaDAO dao = new PessoaDAO();
        lstPessoa = dao.listar();
        cbPessoa.removeAllItems();
        for (int i = 0; i < lstPessoa.size(); i++) {
            cbPessoa.addItem(lstPessoa.get(i).getNome());
        }
    }

    private boolean validarCampos() {
        boolean ret = true;
        if (cbLivro.getSelectedIndex() == -1) {
            ret = false;
            campoObrigatorio = "LIVRO";
            cbLivro.requestFocus();
            return ret;
        }
        if (cbPessoa.getSelectedIndex() == -1) {
            ret = false;
            campoObrigatorio = "PESSOA";
            cbPessoa.requestFocus();
            return ret;
        }
        if (txtDataDevolucao.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "DATA DE DEVOLUCAO";
            txtDataDevolucao.requestFocus();
            return ret;
        }
        return ret;
    }

