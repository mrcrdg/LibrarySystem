    private void btnEmprestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmprestarActionPerformed
        if (validarCampos()) {

            LocalDate dataEmprestimo = LocalDate.now();
            LocalDate dataDevolucao = LocalDate.parse(txtDataDevolucao.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            Livro livro = lstLivro.get(cbLivro.getSelectedIndex());
            Pessoa pessoa = lstPessoa.get(cbPessoa.getSelectedIndex());
            Emprestimo emprestimo = new Emprestimo(livro, pessoa, dataEmprestimo, dataDevolucao);
            EmprestimoDAO dao = new EmprestimoDAO();
            dao.emprestar(emprestimo);
            getLivro();
            getPessoa();
            txtDataDevolucao.setText("");
            JOptionPane.showMessageDialog(this, "Emprestimo realizado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "O Campo " + campoObrigatorio + " é de preenchimento obrigatorio!");
        }
    }//GEN-LAST:event_btnEmprestarActionPerformed

