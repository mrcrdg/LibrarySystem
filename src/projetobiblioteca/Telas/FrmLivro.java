    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validarCampos()) {
            Livro livro = new Livro(txtTitulo.getText(), Integer.parseInt(txtAnoPublicacao.getText()),
                    txtEditora.getText(), Integer.parseInt(txtIsbn.getText()), txtCategoria.getText(), Integer.parseInt(txtQtdExemplar.getText()),
                    chkDisponivel.isSelected());
            LivroDAO dao = new LivroDAO();
            dao.adicionar(livro);
            limparCampos();
            getLivro();
        } else {
            JOptionPane.showMessageDialog(this, "O Campo " + campoObrigatorio + " é de preenchimento obrigatorio!");
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tableLivros.getRowCount() > 0) {
            LivroDAO dao = new LivroDAO();
            dao.Excluir(tableLivros.getSelectedRow());
            getLivro();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

