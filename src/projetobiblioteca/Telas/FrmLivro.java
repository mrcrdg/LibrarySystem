    private void getAutor() {
        AutorDAO dao = new AutorDAO();
        List<Autor> lstAutor = dao.listar();
        cbAutor.removeAllItems();
        for (int i = 0; i < lstAutor.size(); i++) {
            cbAutor.addItem(lstAutor.get(i).getPrimeiroNome());
        }
    }

    private void getLivro() {
        LivroDAO dao = new LivroDAO();
        List<Livro> lstLivro = dao.listar();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Titulo");
        model.addColumn("Ano Publicacao");
        model.addColumn("Editora");
        model.addColumn("ISBN");
        model.addColumn("Categoria");
        model.addColumn("Qtd.Exemplar");
        model.addColumn("Disponivel");
        tableLivros.setModel(model);
        for (Livro livro : lstLivro) {
            Object row[] = {
                livro.getTitulo(),
                livro.getAnoPublicacao(),
                livro.getEditora(),
                livro.getIsbn(),
                livro.getCategoria(),
                livro.getQtdExemplar(),
                livro.isDisponivel()
            };
            model.addRow(row);
        }

    }

    private void limparCampos() {
        txtTitulo.setText("");
        chkDisponivel.setSelected(true);
        txtAnoPublicacao.setText("");
        txtEditora.setText("");
        txtCategoria.setText("");
        txtIsbn.setText("");
        txtQtdExemplar.setText("");
        txtTitulo.requestFocus();
    }

    private boolean validarCampos() {
        boolean ret = true;
        if (txtTitulo.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "TITULO DO LIVRO";
            txtTitulo.requestFocus();
            return ret;
        }
        if (txtAnoPublicacao.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "ANO DE PUBLICACAO";
            txtAnoPublicacao.requestFocus();
            return ret;
        }
        if (txtEditora.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "EDITORA DO LIVRO";
            txtEditora.requestFocus();
            return ret;
        }
        if (txtCategoria.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "CATEGORIA DO LIVRO";
            txtCategoria.requestFocus();
            return ret;
        }
        if (txtIsbn.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "ISBN DO LIVRO";
            txtIsbn.requestFocus();
            return ret;
        }
        if (cbAutor.getSelectedIndex() == -1) {
            ret = false;
            campoObrigatorio = "AUTOR DO LIVRO";
            txtIsbn.requestFocus();
            return ret;
        }
        if (txtQtdExemplar.getText().trim().equals("")) {
            ret = false;
            campoObrigatorio = "QUANTIDADE DE EXEMPLARES";
            txtQtdExemplar.requestFocus();
            return ret;
        }
        return ret;
    }

